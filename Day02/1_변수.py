# 연산을 수행할 때, 자료형에 따라서 가능/불가능이 나눠진다
# 정수와 정수의 덧셈 => 가능
# 정수와 실수의 덧셈 => 가능
# 정수와 문자열의 덧셈 => 불가능
# 문자열과 문자열의 덧셈 => 가능

# 변수이름 = 값
n1 = 1234
n2 = 5678
a1 = '가'
a2 = '나'
f1 = 1.2
f2 = 3.5

print(n1 + n2)  # 숫자끼리는 덧셈
print(a1 + a2)  # 글자끼리는 이어붙이기
print(f1 + f2)  # 실수도 숫자이므로 덧셈




